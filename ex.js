// ------ Helper Methods ------

function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

// Usage
let result1 = add(5, 3);
let result2 = subtract(10, 4);

console.log(`Addition: ${result1}`);  // Output: Addition: 8
console.log(`Subtraction: ${result2}`);  // Output: Subtraction: 6
